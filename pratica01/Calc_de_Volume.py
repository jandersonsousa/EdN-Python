""" 
3- Calculadora de Volume
* Crie um programa que calcula o volume de uma caixa retangular. 
* Use as seguintes dimensões:

* Comprimento: 12 cm
* Largura: 14 cm
* Altura: 20 cm

*O programa deve calcular o volume e exibir o resultado em cm³.
"""

Compri = 12
Larg = 14
Altura = 20

Volume = Compri * Larg * Altura

print("Um retangulo de coprimento: 12 cm, Largura: 14 cm e Altura: 20 cm")
print("O volume desse retangulo é:", Volume, "cm³")
